public class ParallelepipedHW4 {
    public static void main(String[] args){
        double a = 2.23;
        double b = 3.25;
        double c = 1.37;

        double volume = a * b * c;
        System.out.println("Об'єм паралелепіпеда = " + volume);
        double length = 4 * (a + b + c);
        System.out.println("Довжина сторін паралелепіпеда = " + length);

    }

}
